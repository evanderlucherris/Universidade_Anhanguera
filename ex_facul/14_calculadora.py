a = int( input("Primeiro número:" ) )
b = int( input("Segundo número:" ) )

adicao = (a + b)
subtracao = (a - b)
multiplicacao = (a * b)
divisao = (a / b)

print("Resultado da Adição: ",a,"+",b,"=",adicao)
print("Resultado da Subtração: ",a,"-",b,"=",subtracao)
print("Resultado da Multiplicação: ",a,"*",b,"=",multiplicacao)
print("Resultado da Divisão ",a,"/",b,"=",divisao)